/**
 * [Coordinate.java]
 * Represents an ordered pair on a plane
 * @author Edison Du
 * @version 1.0 June 15, 2021
 **/

class Coordinate {
  public int x, y;
  
  /**
   * Coordinate
   * @param x The x position of the coordinate
   * @param y The y position of the coordinate
   */
  Coordinate (int x, int y) {
    this.x = x;
    this.y = y;
  }
}